<h1>İK Stratejmiz ?</h1>
<p>zerO'clock olarak insan kaynakları stratejimiz;</p>

<p>Firmamızın sektöründeki liderliğini sürdürmesi için, kurumsal ortak değerlerimiz olan “Sorumluluk, Saygı ve Sonuç Odaklılık” a uygun, alanında en yetkin çalışanları şirketimize kazandirarak, etkin ödüllendirme, kariyer planlama ve yenilikçi eğitim sistemleriyle gelişimlerini sağlayarak, kurum aidiyetini güçlendirmek, motivasyonu yüksek çalışanlarla hedeflerimize emin adımlarla yürümektir.</p>

<h2>İK Politikamız</h2>
<ul>
	<li>İş gücümüzü; sektörel deneyimi olan, yetkinlikleri gelişmiş doğru kişiler arasindan seçeriz,</li>
	<li>Ailemize katılan çalışanlarımızın, güçlü yönlerinin firmamıza olan katkılarını destekler ve sürekli gelişimlerini hedefleriz,</li>
	<li>Başarıları önemser ve ödüllendirir, gelişmesi gereken yönlerimizi geliştiririz,</li>
	<li>İç iletişimi yüksek tutarak çalışanlarımızın karar alma süreçlerinde katılımcı olmasını destekleriz,</li>
	<li>Aramıza katılan çalışanlarımızım firmaya olan aidiyetlerini güçlendirmeyi hedefleriz,</li>
	<li>Yeni açılan pozisyon ve görevlerde içimizdeki arkadaşlarımıza öncelik tanırız ve kendilerini bu konuda teşvik ederiz.</li>
</ul>
<h2>İşe Alım Süreci</h2>
<p>zerO'clock’ta ise alım süreci “Yanlış insanla doğru işler yapılmaz!” felsefesiyle yürütülür ve “fırsat eşitliği” esasına dayanır. Adaylar “eğitim, deneyim, bilgi, beceri ve karakter özellikleri” kriterlerine uygunluk durumuna göre seçilir.</p>

<p>Sürecimiz, çeşitli kariyer portalları, firma içi network ve mağazalarımızda duyuruların yayınlanmasıyla başlamakta, başvuruların incelenmesi, sıralı mülakatlar, kişilik testleri yapılmasıyla devam etmektedır. Teklif yapılacak adaya referans kontrolü, ücret belirleme çalişmasının yapılması ve is teklifinin sunulması ile sonuçlanmaktadır.</p>

<p>Değerlendirmelerimiz sonunda adaylara mutlaka olumlu ve olumsuz geri bildirim yapılmaktadır.</p>

<p>Şirketimize yapılan tüm başvurular gizlilik ilkesi kapsaminda adalet ve fırsat eşitliği gözetilerek değerlendirilir.</p>
