<h1>İptal ve İade Koşulları</h1>
<p>


Ürün iade istekleri 23/02/1995 tarihli ve 4077 sayılı Tüketicinin Korunması Hakkında Kanunun 31 inci ve bu Kanuna 4822 sayılı Kanunla eklenen 9/A maddeleri esas alınarak aşağıdaki kriterler dâhilinde yapılmaktadır.

Kanunen internetten yapılan satışlarda (mesafeli sözleşmeler ile satışlarda) tüketicilerin teslim aldığı tarihten itibaren 7 gün içerisinde hiçbir hukuki ve cezai sorumluluk üstlenmeksizin ve hiç bir gerekçe göstermeksizin malı reddederek ürünü iade hakkı mevcuttur. zerOc'lock’tan yapılmış olan alışverişlerde 7 güne kadar fatura ibrazı ile kullanılmamış ürünlerde iade konusunda destek sağlamaktayız. İnternet mağazamızdan aldığınız ürünlerin iadeleri sadece internet mağazamıza yapılabilmekte ve mağazalarımızdan iade kabul edilmemektedir. İadelerinizi Yurtiçi Kargo aracılığı ile ücretsiz olarak kolayca yapabilirsiniz.

Değişimler 
İnternet mağazalarımızdan satın aldığınız ürünlerin iadesi mağazalarımızdan kabul edilmemektedir. Yurtiçi Kargo aracılığı ile en kısa sürede ürünü yollayarak müşteri temsilcilerimizin bilgisi dahilinde değişim yapabilirsiniz.

