<h1>Biz Kimiz ?</h1>
<p>Ne zaman hakkında konuşulsa duymanız muhtemel olan bu cümleye imzasını atan zerO'clock 2010 yılında Aydın'da bulunan tek bir mağaza ile temellerini attı. 10 yıla yakın tecrübesi ile günümüz saat sektöründe mağazacılık denince akla ilk gelen firma olmayı başardı.</p>

<p>Kaliteli hizmet anlayışı, yenilikçi duruşu ve müşterisine verdiği güven ile her zaman sadık bir kitle yaratmayı amaç edinmiş zerO'clock; sektörün üretim, tasarım, distribütörlük ve mağazacılık alanında lider firmalarından olan zerO'clock Grubunun gücü ile bugün Türkiye’nin 16 ilinde 41 mağazası bulunan bir zincir haline geldi.</p>

<p>Zincir mağazaları ile büyümeye devam eden zerO'clock, teknolojinin gelişmesiyle beraber 2015 yılında online satış hizmetine başlayarak; %100 orjinal ürünleri, hızlı çözümleri ve teknik servis desteği ile internet kullanıcısının da sevgisini ve güvenini kazanmayı başardı.</p>
