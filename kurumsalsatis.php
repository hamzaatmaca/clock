<h1>Kurumsal Satış ?</h1>
<p>

Yaklaşık 180 mağazamızda 60’ın üzerinde markanın satışını yapıyoruz ve 40’ın üzerinde markanın Türkiye distribütörü konumundayız.

Distribütörlük faaliyetlerimizin yanı sıra üretimini ve dünya genelinde satışını üstlendiğimiz markalarımız ile global pazarda başarılara imza atıyoruz.

Pek çok farklı sektörden, Türkiye’nin ve dünyanın lider şirketleri ile kurumsal iş birlikteliklerimizi başarı ile yürütüyoruz.

Ürünlerimizin ve hediye çeklerimizin kullanıldığı hizmetlerinden bazıları;
<ul>
<li>Kıdem Ödülleri</li>
<li>Çalışan Ödülleri</li>
<li>Müşteri Teşvik ve Sadakat Projeleri</li>
<li>Esnek Yan Haklar</li>
<li>Kurumsal Hediyeler</li>
<li>Firmanıza özel üretim duvar saatleri, masa saatleri ve kol saatleri</li>
Çalışanlarınız ya da müşterileriniz için prestijli ve aynı zamanda unutulmaz bir hediye tercih etmek isterseniz gereken desteği vermekten mutluluk duyacağız.</p>


